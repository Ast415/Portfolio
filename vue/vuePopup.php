            <p>L'objectif de ce travail était de réaliser une étude appliquée de découverte d'un outil de gestion de parc, en particulier de ses fonctionnalités de gestion d'inventaire et de gestion des incidents et demandes (déclaration et suivi). Les étudiants devaient découvrir l'outil GLPI (Gestion Libre de Parc Informatique) et rendre compte de ses fonctionnalités essentielles en alternant entre les rôles d'utilisateur (visiteur médical à terme) et de technicien. Les travaux devaient être réalisés en respectant une succession d'étapes appelées jalons.</p>
